public class stringMethods {
    public static void main(String args[]){
        String s="Patel Heer";
        System.out.println("String="+s);
        System.out.println("String lenght="+s.length());
        System.out.println("Character at index 2="+s.charAt(2));
        System.out.println("Substring for 6="+s.substring(6));
        System.out.println("String uppercase="+s.toUpperCase());
        System.out.println("Lowecase="+s.toLowerCase());
        System.out.println("String contains Heer?="+s.contains("Heer"));
        System.out.println("String replacement="+s.replace("Heer","Sobhana"));
    } 
}
